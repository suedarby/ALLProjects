                        <p> Below is the supply list you will need to draft
                            patterns for small or miniature children dolls. In future posts I will also try to list
                            places
                            to find some of the more unique items such as miniature French Curves suitable for drafting
                            in
                            the smaller scales.
                        </p>
                        <ul>
                            <li>
                                <b>1/8 or 1/4″ Ribbon</b>- used to mark where the bust
                                waist and hips land so that measurements are accurate. To help
                                measure hard to get into places on small dolls. You can lay the
                                ribbon on the doll and then use a pen dot to mark the desired
                                amount and then measure the end to the dot.
                            </li>
                            <li>
                                <b>A plastic coated twist tie</b>- works very well for
                                very tiny dolls as you can bend the wire to the exact amount you
                                need and then use a ruler to do the measuring
                            </li>
                            <li>
                                <b>Eraser</b>– pink pearl or kneaded gum
                            </li>
                            <li>
                                <b>French Curve</b>, tape dispenser, or sets of “doll
                                size” French curves
                            </li>
                            <li>
                                <b>Glue stick</b> helps hold tiny pieces to cardstock
                                for final blocks
                            </li>
                            <li>
                                <b>Light Table</b> or A Box with a Light Inside and
                                Clear Glass or Plastic Over the Top or A Child’s Light Table –
                                used for tracing patterns easier
                            </li>
                            <li>
                                <b>Magnifying glass</b> Lets you see small things easier
                            </li>
                            <li>
                                <b>Manila Envelopes</b>, Thin Cardboard, Card Stock Or
                                Junk Mail Post Cards Or Thin Cardboard Boxes- for creating
                                permanent blocks
                            </li>
                            <li>
                                <b>Measuring Tape</b> – a normal sized human one works
                                well. Alternately the retractable purse size measuring tape found
                                at Wal-Mart works very well too.
                            </li>
                            <li>
                                <b>Muslin</b>– relatively inexpensive way to do test
                                fits and be able to sew the pieces together along with marking on
                                the pieces any changes*Paper- light weight for first drafts of the
                                pattern
                            </li>
                            <li>
                                <b>Paper Towel,</b> muslin, used dryer sheets-
                                inexpensive way to test patterns
                            </li>
