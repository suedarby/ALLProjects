                <div class="card-header" id="glossaryHeading">
                    <h2 class="mb-0">
                        <button class="btn btn-link btn-block text-left collapsed" type="button" data-toggle="collapse"
                            data-target="#glossary" aria-expanded="false" aria-controls="glossaryHeading">Glossary of
                            Terms
