                        <p>Since most dolls are evenly made on both sides, the measurements are taken on the half or in
                            the
                            case of an arc the quarter. The exception is circumference measurements, which are all the
                            way
                            around the body. It is advisable that you mark somehow, either with scotch tape on the body
                            and
                            a pencil mark or however you are comfortable, the center front and back along with where
                            your
                            doll’s bust, waist and hip fall. Deciding this before you start measuring will make a big
                            difference in how your patterns will turn out and how accurate they will be. For scale and
                            1/2
                            scale miniatures use metrics, as a millimeter is more accurate than rounding to the nearest
                            fraction. When working with dolls this tiny it is imperative that your measurements be
                            accurate.
                        </p>
