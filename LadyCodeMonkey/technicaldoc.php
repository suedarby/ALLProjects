                <p>This tutorial was originally created in 2000-2001 and tested on my
                    daughter at about a year old (she's 21 now). I have also used this
                    tutorial to draft for a doll as small as 1/2" tall, of course using
                    magnifying glasses and a very sharp pencil! This is a preview of the
                    techniques used in
                    <a title="Pattern Making for Dolls" href="http://suestinycostumes.com/pattern-making-for-dolls"
                        rel="noopener">
                        Pattern Making for Dolls</a>
                    and
                    <a title="Pattern Drafting For Miniatures"
                        href="http://suestinycostumes.com/pattern-drafting-for-miniatures" rel="noopener">
                        Pattern Drafting For Miniatures</a>
                    which were originally sold as hard copy books, moved to CD and are
                    now available only in PDF as an instant download. Included in this
                    tutorial series are excerpts all the items that will be needed for
                    all the pattern drafts for a child doll. It is suitable for a 6" or
                    taller doll.
                </p>
                <p>
                    Each of the following sections will include the first portion of
                    each draft with a link to the full tutorial which has been published
                    on Sue's Tiny Costumes for 20 years. This is the longest and was the
                    easiest to translate into this project to use as a sample of both
                    writing and code work for the project.
