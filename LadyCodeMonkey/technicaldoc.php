                    </h2>
                </div>
                <div id="glossary" class="collapse" aria-labelledby="glossaryHeading" data-parent="#PDAccordion">
                    <div class="card-body">
                        <ul>
                            <li>
                                <b>Abdomen</b>– area between the waist and hips around the belly button
                                area
                            </li>
                            <li><b>Abdomen arc</b> – 1/4 of the total circumference of the fattest part
                            </li>
                            <li><b>Apex</b> – the doll’s nipple if she has one or the tip of her breast
                            </li>
                            <li><b>Arc</b>– 1/4 of complete circumference measurement
                            </li>
                            <li><b>Banana dart</b>– a dart that looks like a straight up and down banana
                            </li>
                            <li><b>Basic block</b>– your pattern that you drafted from your measurements
                                it has no design to it
                            </li>
                            <li><b>Bias grain</b>– the diagonal of the fabric and your grain line is lined
                                up with it for a different drape
                            </li>
                            <li><b>Blend</b>– making separate lines look like 1 continuous one
                            </li>
                            <li><b>Bust</b>– chest level on a child or man or the breasts of a lady doll
                            </li>
                            <li>
                                <b>Bust arc</b>– the distance from the flat ribs
                                below the bust to the apex
                            </li>
                            <li><b>Bust bridge</b>– distance between apexes
                            </li>
                            <li>
                                <b>Cap ease</b> – difference between cap and armhole
                                measurement
                            </li>
                            <li><b>Cap</b>– height distance from biceps to cap at center
                            </li>
                            <li>
                                <b>Center bac</b>k- center of the back usually
                                where there would be a spine
                            </li>
                            <li>
                                <b>Center front</b> -center of the front of a doll
                                where there would normally be a breast bone
                            </li>
                            <li>
                                <b>Circumference</b>– distance around somewhere
                            </li>
                            <li>
                                <b>Cloth</b> <b>body</b>– the body is made of cloth and is very
                                soft and huggable.
                            </li>
                            <li>
                                <b>Composition</b> <b>body</b>– the body IS made of a plastic
                                substance and does not squish when you hug your doll
                            </li>
                            <li>
                                <b>Cross grain</b>– grain running from selvage to selvage
                            </li>
                            <li>
                                <b>Crotch</b>– area where a drink and wet doll wets and where panties
                                would normally go
                            </li>
                            <li>
                                <b>Dart intake</b>– the extra added to a pattern so that when you sew the
                                dart it doesn’t end up too small
                            </li>
                            <li>
                                <b>Dart leg</b>– one of the lines that makes up a dart
                            </li>
                            <li>
                                <b>Dart point</b>– the tip of the dart
                            </li>
                            <li>
                                <b>Darts</b>– used to fit a garment close to the body primarily for lady
                                dolls but can be used on children or men but never on a baby.
                            </li>
                            <li>
                                <b>Drape</b>– holding and pinning a piece of fabric up to a doll and
                                pinching the material until it fits then marking where darts are and making a basic
                                pattern from the fabric markings in a connect-the-dots style.
                            </li>
                            <li>
                                <b>Ease</b>– the extra bit of room that allows you
                                to dress the doll without breaking her
                            </li>
                            <li>
                                <b>Elbow level</b> – elbow of doll
                            </li>
                            <li>
                                <b>Finger span</b>– the distance around all the fingers at the largest
                                point
                            </li>
                            <li>
                                <b>French curve</b>– plastic tool used to draw curves various sizes are
                                available including ones specifically for dolls
                            </li>
                            <li>
                                <b>Grade</b>– to enlarge or shrink an current pattern
                            </li>
                            <li>
                                <b>Grading</b>– the act of enlarging or shrinking a pattern
                            </li>
                            <li>
                                <b>Grain</b> <b>line</b>– center of garment running normally
                                from top to bottom of piece
                            </li>
                            <li>
                                <b>Hip arc</b>– 1/4 of the total
                                hip measurement
                            </li>
                            <li>
                                <b>Horizontal balance lines or HBL</b>– horizontal lines used as a basis
                                of where the bust waist and hip lie so that measuring is more accurate and
                                easier
                            </li>
                            <li>
                                <b>Notches</b>– used at the armhole and top of sleeve to ensure that the
                                sleeve doesn’t end up crooked when sewn
                            </li>
                            <li>
                                <b>Porcelain</b> <b>body</b>– made of porcelain doesn’t squish
                                and is very hard similar to a composition body only very fragile
                            </li>
                            <li>
                                <b>Princess</b> <b>line</b>– the style of a pattern where the
                                bodice or skirt has been split into 2 pieces for each quarter of the body
                            </li>
                            <li>
                                <b>Raglan</b>– a style where the sleeve doesn’t come from the shoulder tip
                                but from the neck shoulder junction as in a sweatshirt
                            </li>
                            <li>
                                <b>Right angle or RT angle</b>– a 90-degree angle commonly found at
                                necklines centers side seams and armhole bottoms
                            </li>
                            <li>
                                <b>Rulers</b>– measuring tool
                            </li>
                            <li>
                                <b>Seam</b>– sewn together pieces of fabric to form
                                1 piece
                            </li>
                            <li>
                                <b>Seam allowance or S/A</b>– allowance of extra fabric so that your
                                sewing machine has a little extra to grab on to when it tries to feed your fabric
                                through.
                            </li>
                            <li>
                                <b>Sleeve cap</b> -the curved top section of the sleeve from the front to
                                the back
                            </li>
                            <li>
                                <b>Sleeve ease</b>– the added room needed to allow the arm to move if
                                necessary
                            </li>
                            <li>
                                <b>Straight grain</b>– the vertical grain of the
                                fabric
                            </li>
                            <li>
                                <b>Style lines</b>– various lines made on patterns to create a new look or
                                design
                            </li>
                            <li>
                                <b>Torso</b>– the body part of a doll with out the head, arms, or legs.
                            </li>
                            <li>
                                <b>Truing the pattern</b>– checking to be sure that all areas match up
                                side seams are the same length, shoulder seams are the same etc.
                            </li>
                            <li>
                                <b>Waist arc</b>– 1/4 of the total waist circumference measurement
                            </li>
                            <li>
                                <b>Wrist level</b>– the bottom hemline area of a sleeve, level with the
                                wrist of the arm
                            </li>
                        </ul>
