                    <div class="card-body">
                        <p>Here are some suggestions for some common items that can make drafting and sewing for smaller
                            dolls easier!</p>
                        <ol>>
                            <li>Dryer sheet — can be used for any spot that requires interfacing of sorts as in collars
                                and
                                cuffs but should not be used for the entire garment. I have tried it several times and
                                cutting it away sometimes ends in holes in the actual garment no matter how careful I
                                am.
                            </li>
                            <li>Tear away stabilizer— this is useful for china silk that is slightly heavy yet still
                                slippery. The bad thing is that tearing it away sometimes will distort the stitching and
                                fabric.</li>
                            <li>Water Soluble stabilizer — my latest discovery! This stuff is easy to use simply trace
                                the
                                pieces to the stabilizer and construct the garment. When ready to get rid of the
                                stabilizer
                                simply dunk in cold water and it all dissolves like magic leaving soft silk in it’s
                                place.
                                It is also handy to use in bodice construction as when you turn bodices sometimes pointy
                                or
                                even dull tools can poke through the fabric the stabilizer helps to prevent this thus
                                avoiding the ruin of a lot of work! So far I haven’t found a downside to this yet.</li>
                            <li>The 2 stabilizers mentioned above are available in the machine embroidery section of any
                                sewing or craft store and are usually very light weight. No, I do not sell the items
                                mentioned I just use it and love it!</li>
                            <li>Machine basting patterns to fabric is a lot of work. I’ve found that hand basting is
                                faster
                                and less of a hassle as you do not have to pin the pieces to the fabric first. Slightly
                                larger stitches are ok for this too as you want to be able to see to take them out
                                later!
                            </li>
                            <li>Best tools I’ve found so far are crochet hooks. A size 5 crochet hook has a blunt butt
                                end
                                that is smaller than a bodkin which is useful also but the crochet hook also has a
                                rounded
                                end by the hook that is great for getting bodice pieces to turn nicely too. This is
                                especially important for half inch scale bodices!</li>
                            <li>Got small kids in the house? I do! Mischief makers both. Now being that I sew and do it
                                A
                                LOT I use needles and pins…. standard pin cushions don’t baby proof enough to suit me.
                                My
                                solution is to take the nice childproof prescription medicine bottles and put pins and
                                needles in those. I have a nice fat one for pins and several skinny ones for various
                                types
                                of needles including sewing machine needles. This makes good storage…and the kids can’t
                                get
                                into them!</li>
                            <li>Alternate to ribbon for measuring small dolls accurately is a twist tie.To measure
                                simply
                                place one end at the starting point and the other end you bend then measure the tiny
                                portion
                                before the bend against an accurate ruler.
                            </li>
