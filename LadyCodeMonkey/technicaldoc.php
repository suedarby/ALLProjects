                        <p>Due to the many requests I’ve had during classes on information about metrics vs standard
                            measuring I decided that a tutorial was necessary for measuring in metrics as it is more
                            accurate for very small dolls.</p>
                        <p>To use the chart found at the link:</p>
                        <ul>
                            <li>Fractions are obvious I would guess.</li>
                            <li>The decimal was found by dividing 1 by the number after the / so 1 divided by 64 gives
                                you
                                the .0156.</li>
                            <li>The MM was found by multiplying by 2.54. To read the MM section correctly you read after
                                the
                                decimal as mm and before the . as CM. Thus 1.3 cm is 1 centimeters and 3 millimeters.
                            </li>
                            <li>My books and other drafting tutorials are written for standard thus this is to help
                                convert
                                the fractions to a more manageable format. When reading a long MM measurement such as
                                25.7969 you will want to round the .7969 to .8 thus 25 cm plus 8 mm is what you will
                                find
